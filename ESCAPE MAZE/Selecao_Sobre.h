void selectAbout(void)
{
    int select = 0;
    //Jogo desenvolvido como Trabalho final para a disciplina EMB5642-Microcontroladores.
    while(1)
    {
        if( get_matriz_botao(1000) == LOW )
        {
            SysCtlDelay( 2500000 );
            select++;
            if( select > 2 ) select--;
        }

        else if( get_matriz_botao(1000) == HIGH )
        {
            SysCtlDelay( 2500000 );
            select--;
            if( select < 0 ) select++;
        }

        switch(select)
        {
           case 0:
                Nokia5110_ClearBuffer();
                Nokia5110_SetCursor(0,2);
                Nokia5110_OutString("Jogo desenvolvido como Trabalho ...");
                SysCtlDelay( 100000 );

                Nokia5110_PrintBMP2(0,0,OptionAbout,84,48);
                Nokia5110_DisplayBuffer();
                break;

           case 1:
               Nokia5110_ClearBuffer();
               Nokia5110_SetCursor(0,2);
               Nokia5110_OutString("... final para a disciplina ...");
               SysCtlDelay( 100000 );

               Nokia5110_PrintBMP2(0,0,OptionAbout,84,48);
               Nokia5110_DisplayBuffer();
               break;

           case 2:
              Nokia5110_ClearBuffer();
              Nokia5110_SetCursor(0,2);
              Nokia5110_OutString("... EMB5642-Microcontroladores.");
              SysCtlDelay( 100000 );

              Nokia5110_PrintBMP2(0,0,OptionAboutEnd,84,48);
              Nokia5110_DisplayBuffer();
              break;

           default:
               break;
        }

        if(get_matriz_botao(1000) == BACK) break; //caso queira sair do menu sobre
    }
}

